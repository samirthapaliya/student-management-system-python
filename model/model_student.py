class student_detail:
    def __init__(self, Student_Id, First_name, Last_name, Gender, Dob, Contact, Email, Parent_name, Parent_contact,
                 Address):
        self.Student_Id = Student_Id
        self.First_name = First_name
        self.Last_name = Last_name
        self.Gender = Gender
        self.Dob = Dob
        self.Contact = Contact
        self.Email = Email
        self.Parent_name = Parent_name
        self.Parent_contact = Parent_contact
        self.Address = Address

    def set_Student_Id(self, Student_Id):
        self.Student_Id = Student_Id

    def get_Student_Id(self):
        return self.Student_Id

    def set_First_name(self, First_name):
        self.First_name = First_name

    def get_First_name(self):
        return self.First_name

    def set_Last_name(self, Last_name):
        self.Last_name = Last_name

    def get_Last_name(self):
        return self.Last_name

    def set_Gender(self, Gender):
        self.Gender = Gender

    def get_Gender(self):
        return self.Gender

    def set_Dob(self, Dob):
        self.Dob = Dob

    def get_Dob(self):
        return self.Dob

    def set_Contact(self, Contact):
        self.Contact = Contact

    def get_Contact(self):
        return self.Contact

    def set_Email(self, Email):
        self.Email = Email

    def get_Email(self):
        return self.Email

    def set_Parent_name(self, Parent_name):
        self.Parent_name = Parent_name

    def get_Parent_name(self):
        return self.Parent_name

    def set_Parent_contact(self, Parent_contact):
        self.Parent_contact = Parent_contact

    def get_Parent_contact(self):
        return self.Parent_contact

    def set_Address(self, Address):
        self.Address = Address

    def get_Address(self):
        return self.Address

